clear;
data  = xlsread('warp_1.xlsx', 'A2:M13');
distance1=zeros(7,1);
warp_ber_los=zeros(7,1);
warp_throughput_los=zeros(7,1);
distance1(1:7,1)=data(1:7,1);
warp_throughput_los(1:7,1)=(data(1:7,7).*data(1:7,9)+data(1:7,8).*data(1:7,10))./data(1:7,4)*4e6/2;
warp_ber_los(1:7,1)=[1 1 1 1 1 1 1]'-data(1:7,11);
% [hAx,hLine1,hLine2]=plotyy(distance,warp_ber_los,distance,warp_throughput_los);
% set(hLine1,'Marker','*');
% set(hLine2,'Marker','^');
% ylabel(hAx(1),'BER');
% ylabel(hAx(2),'Throughput');
% xlabel('Distance(m)');
% title('BER and throughout of different distance');

distance2=zeros(5,1);
warp_ber_nlos=zeros(5,1);
warp_throughput_nlos=zeros(5,1);
distance2(1:5,1)=data(8:12,1);
warp_throughput_nlos(1:5,1)=(data(8:12,7).*data(8:12,9)+data(8:12,8).*data(8:12,10))./data(8:12,4)*4e6/2;
warp_ber_nlos(1:5,1)=[1 1 1 1 1]'-data(8:12,11);
figure;
% [hAx,hLine1,hLine2]=plotyy(distance,warp_ber,distance,warp_throughput);
% set(hLine1,'Marker','*');
% set(hLine2,'Marker','^');
% ylabel(hAx(1),'BER');
% ylabel(hAx(2),'Throughput');
% xlabel('Distance(m)');
% title('BER and throughout of different distance');
figure;
plot(distance1,warp_ber_los,'-*');
hold on;plot(distance2,warp_ber_nlos,'-^');
legend('ber_los','ber_nlos');
ylabel('BER');
xlabel('Distance(m)');
title('BER of los and nlos(warp)');
figure;
plot(distance1,warp_throughput_los,'-*');
hold on;plot(distance2,warp_throughput_nlos,'-^');
legend('throughput_los','throughput_nlos');
ylabel('Throughput');
xlabel('Distance(m)');
title('Throughput of los and nlos(warp)');
